// 終端追従の許容差（px）。
//
// scrollLeft/scrollTop はブラウザがデバイスピクセル（整数）へ丸めるため、
// 「末尾に到達したか」を maxScroll との厳密一致では判定できない。この微小な
// イプシロンで丸め誤差だけを吸収する。
//
// 旧 `followSnapPx`（ユーザー設定・既定 50px）を廃止して固定の小さな値に統一した。
// 大きいしきい値は終端付近に「follow に張り付くデッドゾーン」を作り、終端から
// 上方向へ少しスクロールしても即座に follow へ引き戻されて手動スクロールに
// 抜けられなくなる問題があった（スクロールバー上ボタンの増分がしきい値に届かない等）。
// イプシロンを 2px まで下げるとデッドゾーンが知覚されない幅になり、
// 「末尾に到達したら follow / 末尾から離れたら manual」という素直な挙動になる。
export const END_EPS = 2;
